    console.log('‚úÖ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –æ–±–ª–∞—á–Ω–æ–π –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö Supabase —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ');
    
